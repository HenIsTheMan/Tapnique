namespace IWP.Math {
	internal static partial class Trigo {
		#region Fields
		#endregion

		#region Properties
		#endregion

		public static float Csc(float x) {
			return 1.0f / UnityEngine.Mathf.Sin(x);
		}

		public static float Cosec(float x) {
			return 1.0f / UnityEngine.Mathf.Sin(x);
		}

		public static float Cosecant(float x) {
			return 1.0f / UnityEngine.Mathf.Sin(x);
		}
	}
}
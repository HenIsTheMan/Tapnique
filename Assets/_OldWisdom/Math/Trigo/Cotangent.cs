namespace IWP.Math {
	internal static partial class Trigo {
		#region Fields
		#endregion

		#region Properties
		#endregion

		public static float Cot(float x) {
			return 1.0f / UnityEngine.Mathf.Tan(x);
		}

		public static float Cotangent(float x) {
			return 1.0f / UnityEngine.Mathf.Tan(x);
		}
	}
}